package utcdate

import "time"

// DateUTC returns UTC midnight for the given instant's calendar date.
func DateUTC(t time.Time) time.Time {
	t = t.UTC()
	return time.Date(t.Year(), t.Month(), t.Day(), 0, 0, 0, 0, time.UTC)
}

// YesterdayUTC returns yesterday's calendar date at UTC midnight.
func YesterdayUTC(now time.Time) time.Time {
	return DateUTC(now.UTC().AddDate(0, 0, -1))
}
